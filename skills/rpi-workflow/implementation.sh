#!/bin/sh
# RPI Workflow - Implementation 阶段辅助脚本
# 用于记录实现过程

set -e

if [ $# -lt 2 ]; then
    echo "用法: $0 <项目目录> <计划ID>"
    echo "示例: $0 . 20260419-101000-20260419-100000-weather-page"
    exit 1
fi

PROJECT_DIR="$1"
PLAN_ID="$2"
RPI_DIR="$PROJECT_DIR/.rpi"
PLAN_FILE="$RPI_DIR/plans/$PLAN_ID.md"
TIMESTAMP=$(date +"%Y%m%d-%H%M%S")
IMPL_FILE="$RPI_DIR/implementations/$TIMESTAMP-$PLAN_ID.md"

if [ ! -f "$PLAN_FILE" ]; then
    echo "❌ 错误：未找到计划文件 $PLAN_FILE"
    echo ""
    echo "可用计划："
    ls -1 "$RPI_DIR/plans/" 2>/dev/null | sed 's/.md$//' || echo "  (无)"
    exit 1
fi

echo "🚀 Implementation 阶段开始..."
echo "计划: $PLAN_ID"
echo ""

# 提取计划标题
PLAN_TITLE=$(grep -m1 "^# 执行计划：" "$PLAN_FILE" | sed 's/^# 执行计划：//' || echo "$PLAN_ID")

# 生成实现记录模板
echo "📝 生成实现记录文档..."

cat > "$IMPL_FILE" <<EOF
# 实现记录：$PLAN_TITLE

**创建时间**: $(date +"%Y-%m-%d %H:%M:%S")  
**关联计划**: $PLAN_ID  
**状态**: In Progress

---

## 关联计划

\`$PLAN_FILE\`

---

## 实现进度

### 已完成任务

#### 任务 1: [任务名称]
- **完成时间**: $(date +"%Y-%m-%d %H:%M:%S")
- **使用模型**: 待记录
- **代码变更**: 
  - 待记录
- **审查结果**: 
  - 模型 A: 待记录
  - 模型 B: 待记录
- **验证状态**: ✅ 通过 / ❌ 失败

---

### 进行中任务

#### 任务 X: [任务名称]
- **开始时间**: $(date +"%Y-%m-%d %H:%M:%S")
- **当前状态**: 待更新

---

### 待执行任务

- [ ] 任务 Y
- [ ] 任务 Z

---

## 代码变更摘要

### 新增文件
- 待记录

### 修改文件
- 待记录

### 删除文件
- 待记录

---

## 多模型协作记录

### 原型生成

#### 任务 1 - 模型 A
- **模型**: 待记录
- **提示词**: 待记录
- **生成结果**: 待记录
- **是否采用**: 是/否（重写）

---

### 代码审查

#### 任务 1 - 审查轮次 1
- **审查模型**: 待记录
- **发现问题**: 
  - 待记录
- **修改建议**: 
  - 待记录
- **审查结果**: LGTM / 需修改

---

## 遇到的问题与解决方案

### 问题 1
- **描述**: 待记录
- **影响**: 待记录
- **解决方案**: 待记录
- **状态**: 已解决 / 待解决

---

## 上下文使用情况

| 时间点 | 上下文使用量 | 操作 |
|--------|-------------|------|
| $(date +"%H:%M:%S") | 待记录 | 开始实现 |

---

## 测试验证

### 单元测试
- [ ] 待补充

### 集成测试
- [ ] 待补充

### PBT 验证
- [ ] 属性 1: 待验证
- [ ] 属性 2: 待验证

---

## 完成标准

- [ ] 所有任务已完成
- [ ] 所有代码审查通过（双模型 LGTM）
- [ ] 所有测试通过
- [ ] 所有 PBT 属性验证通过
- [ ] 代码风格一致
- [ ] 文档更新完成

---

## 下一步

- [ ] 继续执行剩余任务
- [ ] 进行集成测试
- [ ] 部署到测试环境
- [ ] 用户验收测试

EOF

echo "✅ 实现记录已创建: $IMPL_FILE"
echo ""
echo "📋 实现记录 ID: $TIMESTAMP-$PLAN_ID"
echo ""
echo "💡 下一步："
echo "   1. 按计划顺序执行任务"
echo "   2. 每完成一个任务更新实现记录"
echo "   3. 使用多模型生成原型并重写为生产代码"
echo "   4. 每个任务完成后进行双模型审查"
echo "   5. 监控上下文使用量，及时清空"
echo "   6. 所有任务完成后进行集成测试"
