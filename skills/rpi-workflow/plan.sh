#!/bin/sh
# RPI Workflow - Plan 阶段辅助脚本
# 用于将提案细化为零决策执行计划

set -e

if [ $# -lt 2 ]; then
    echo "用法: $0 <项目目录> <提案ID>"
    echo "示例: $0 . 20260419-100000-weather-page"
    exit 1
fi

PROJECT_DIR="$1"
PROPOSAL_ID="$2"
RPI_DIR="$PROJECT_DIR/.rpi"
PROPOSAL_FILE="$RPI_DIR/proposals/$PROPOSAL_ID.md"
TIMESTAMP=$(date +"%Y%m%d-%H%M%S")
PLAN_FILE="$RPI_DIR/plans/$TIMESTAMP-$PROPOSAL_ID.md"

if [ ! -f "$PROPOSAL_FILE" ]; then
    echo "❌ 错误：未找到提案文件 $PROPOSAL_FILE"
    echo ""
    echo "可用提案："
    ls -1 "$RPI_DIR/proposals/" 2>/dev/null | sed 's/.md$//' || echo "  (无)"
    exit 1
fi

echo "📋 Plan 阶段开始..."
echo "提案: $PROPOSAL_ID"
echo ""

# 提取提案标题
PROPOSAL_TITLE=$(grep -m1 "^# 提案：" "$PROPOSAL_FILE" | sed 's/^# 提案：//' || echo "$PROPOSAL_ID")

# 生成计划模板
echo "📝 生成执行计划文档..."

cat > "$PLAN_FILE" <<EOF
# 执行计划：$PROPOSAL_TITLE

**创建时间**: $(date +"%Y-%m-%d %H:%M:%S")  
**关联提案**: $PROPOSAL_ID  
**状态**: Draft

---

## 关联提案

\`$PROPOSAL_FILE\`

---

## 技术决策（零模糊性）

### 技术栈
<!-- 具体技术 + 版本 + 配置 -->
- **语言**: 待定
- **框架**: 待定
- **库/依赖**: 待定

### 算法选择
<!-- 算法 + 参数 -->
- 待补充

### 行为规则
<!-- 精确规则，无模糊性 -->
- 待补充

---

## 任务流（顺序执行，无决策点）

### 任务 1: [任务名称]
- **描述**: 待补充
- **输入**: 待补充
- **输出**: 待补充
- **验证标准**: 待补充
- **预计复杂度**: 低/中/高

### 任务 2: [任务名称]
- **描述**: 待补充
- **输入**: 待补充
- **输出**: 待补充
- **验证标准**: 待补充
- **预计复杂度**: 低/中/高

<!-- 继续添加任务... -->

---

## PBT 属性（Property-Based Testing）

### 属性 1: [属性名称]
- **定义**: 待补充（形式化描述）
- **不变量**: 待补充（必须始终成立的条件）
- **边界条件**: 待补充（边缘情况）
- **证伪策略**: 待补充（如何生成反例）

### 属性 2: [属性名称]
- **定义**: 待补充
- **不变量**: 待补充
- **边界条件**: 待补充
- **证伪策略**: 待补充

---

## 多模型分析记录

### 模型 A 分析（模糊性检测）
- **模型**: 待指定
- **发现的模糊性**: 
  - [ ] 待补充
- **需要的约束**: 
  - [ ] 待补充

### 模型 B 分析（隐含假设）
- **模型**: 待指定
- **发现的假设**: 
  - [ ] 待补充
- **需要的显式约束**: 
  - [ ] 待补充

---

## 反模式检测

### 已拒绝的反模式
- [ ] 信息收集无决策边界
- [ ] 技术对比无选择标准
- [ ] 推迟决策标记为"实现时确定"

### 已确认的目标模式
- [ ] 显式技术选择 + 参数
- [ ] 具体算法选择 + 配置
- [ ] 精确行为规则

---

## 退出标准

- [ ] 所有多模型分析完成并综合
- [ ] 零模糊性（经审计验证）
- [ ] 所有 PBT 属性已记录证伪策略
- [ ] 用户已明确批准所有约束决策
- [ ] 任务流可零决策顺序执行

---

## 下一步

- [ ] 使用多模型分析提案
- [ ] 解决所有模糊性和隐含假设
- [ ] 定义所有 PBT 属性
- [ ] 用户确认技术决策
- [ ] 进入 Implementation 阶段

EOF

echo "✅ 执行计划已创建: $PLAN_FILE"
echo ""
echo "📋 计划 ID: $TIMESTAMP-$PROPOSAL_ID"
echo ""
echo "💡 下一步："
echo "   1. 使用多模型分析提案（检测模糊性和假设）"
echo "   2. 完成技术决策（消除所有决策点）"
echo "   3. 定义任务流（顺序执行，无分支）"
echo "   4. 提取 PBT 属性（针对后端逻辑）"
echo "   5. 用户确认后进入 Implementation 阶段"
